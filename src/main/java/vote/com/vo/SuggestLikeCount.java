package vote.com.vo;

public class SuggestLikeCount {
	private int suggestNo;
	private int count;
	
	public int getSuggestNo() {
		return suggestNo;
	}
	public SuggestLikeCount setSuggestNo(int suggestNo) {
		this.suggestNo = suggestNo;
		return this;
	}
	public int getCount() {
		return count;
	}
	public SuggestLikeCount setCount(int count) {
		this.count = count;
		return this;
	}
}
